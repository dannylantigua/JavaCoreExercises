public class SimpleCalculator {
    
    public int addition(int num1, int num2){
        return num1 + num2;
    }
    
    public int substraction (int num1, int num2){
        return num1 - num2;
    }
    
    public int multiplication(int num1, int num2){
        return num1 * num2;
    }
    
    public int division (int num1, int num2){
        return num1 / num2;
    }
}
